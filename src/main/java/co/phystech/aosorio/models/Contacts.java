package co.phystech.aosorio.models;

/**
 * @author AOSORIO
 *
 */
public class Contacts {
	
	private String name;
	private String address;
	private String phone;
	private String country;
	private String countryCode;
	private String city;
	private String coordinates;
	private String webpage;
	private String emailAddresses;
	private boolean hasDataProtection;
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the country
	 */
	public String getCountry() {
		return country;
	}
	/**
	 * @param country the country to set
	 */
	public void setCountry(String country) {
		this.country = country;
	}
	/**
	 * @return the countryCode
	 */
	public String getCountryCode() {
		return countryCode;
	}
	/**
	 * @param countryCode the countryCode to set
	 */
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	/**
	 * @return the city
	 */
	public String getCity() {
		return city;
	}
	/**
	 * @param city the city to set
	 */
	public void setCity(String city) {
		this.city = city;
	}
	/**
	 * @return the coordinates
	 */
	public String getCoordinates() {
		return coordinates;
	}
	/**
	 * @param coordinates the coordinates to set
	 */
	public void setCoordinates(String coordinates) {
		this.coordinates = coordinates;
	}
	/**
	 * @return the webpage
	 */
	public String getWebpage() {
		return webpage;
	}
	/**
	 * @param webpage the webpage to set
	 */
	public void setWebpage(String webpage) {
		this.webpage = webpage;
	}
	/**
	 * @return the emailAddresses
	 */
	public String getEmailAddresses() {
		return emailAddresses;
	}
	/**
	 * @param emailAddresses the emailAddresses to set
	 */
	public void setEmailAddresses(String emailAddresses) {
		this.emailAddresses = emailAddresses;
	}
	/**
	 * @return the hasDataProtection
	 */
	public boolean isHasDataProtection() {
		return hasDataProtection;
	}
	/**
	 * @param hasDataProtection the hasDataProtection to set
	 */
	public void setHasDataProtection(boolean hasDataProtection) {
		this.hasDataProtection = hasDataProtection;
	}

	
	
}
